void main() {
    var day = 6;

    switch (day) {
        case 1:
            print("Today is Monday, Let's start the week.");
            break;
        case 2:
            print("It is Tuesday, Let's continue the grind.");
            break;
        case 3:
            print("It is Wednesday, Another day of the week.");
            break;
        case 4:
            print("It is Thursday, We are closer to the weekend.");
            break;
        case 5:
            print("It is Friday, Yay, let's dance!");
            break;
        default:
            print("It is the weekend, Let's chill!");
    }
}